export function generateWeeklyPlan(cuisine: string): WeeklyPlan {
  const days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday'];
  const cuisineMeals = mockMeals[cuisine] || mockMeals.korean;
  
  return {
    id: `plan-${Date.now()}`,
    cuisine,
    createdAt: new Date(),
    days: days.map(day => ({
      day,
      breakfast: mockMeals.breakfast[Math.floor(Math.random() * mockMeals.breakfast.length)],
      lunch: cuisineMeals[0],
      dinner: cuisineMeals[1] || cuisineMeals[0],
      snacks: [mockMeals.snacks[0], mockMeals.snacks[1]],
    })),
  };
}

